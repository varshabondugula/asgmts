package com.AbhinavAssignmentPrograms;

import java.util.LinkedList;
import java.util.List;

public class ListUsingForEach {
	
	
	

	
	public static void main(String s[]) {
		
		List<String> l=new LinkedList<>();
		l.add("varsha");
		l.add("vandana");
		l.add("naveen");
		
		for (String str:l) {
			System.out.println(str);		
		
		}
	
		
	}
}
